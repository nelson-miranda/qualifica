    <p>
      A &#225;rea da sa&#250;de possui diversos padr&#245;es estabelecidos para a troca de dados e informa&#231;&#245;es, sendo o mais conhecido a CID-10. No Brasil, s&#227;o exemplos a Tabela SUS (Tabela de Procedimentos, Medicamentos, &#211;rteses Pr&#243;teses e Materiais Especiais do Sistema &#218;nico de Sa&#250;de) e o TISS (Padr&#227;o para a Troca de Informa&#231;&#227;o de Sa&#250;de Suplementar). Este segundo, voltado para a troca de informa&#231;&#245;es entre os prestadores de servi&#231;o na &#225;rea da sa&#250;de e as operadoras de planos privados de sa&#250;de, estabelecido pela Ag&#234;ncia Nacional de Sa&#250;de Suplementar (ANS).
