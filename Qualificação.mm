    </p>
    <p>
      Do ponto de vista da sintaxe, &#233; poss&#237;vel serializar documentos OWL nos formatos: RDF/XML (padr&#227;o), OWL/XML, Turtle, entre outros. Sempre considerando aspectos importantes da modelagem de conhecimento, tais como: entidades, express&#245;es e axiomas.
    </p>
    <p>
