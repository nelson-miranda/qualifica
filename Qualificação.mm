<node TEXT="Ontologias" ID="ID_1215333843" CREATED="1543688194163" MODIFIED="1543693635346"><richcontent TYPE="NOTE">

<html>
  <head>
    
  </head>
  <body>
    <p>
      Apontada como o terceiro componente b&#225;sico da Web Sem&#226;ntica, as Ontologias s&#227;o cole&#231;&#245;es de informa&#231;&#245;es. Origin&#225;rias da Metaf&#237;sica, &#225;rea da Filosofia, que estuda a natureza do ser e a exist&#234;ncia.
    </p>
    <p>
      Na Computa&#231;&#227;o, uma ontologia &#233; um conjunto de conceitos e suas rela&#231;&#245;es que busca representar o entendimento de um dom&#237;nio, atrav&#233;s de uma representa&#231;&#227;o formal compreens&#237;vel tanto por humanos quanto por computadores.
    </p>
    <p>
      Segundo Gruber (1993), ontologia &#233; uma especifica&#231;&#227;o expl&#237;cita de um conceitualiza&#231;&#227;o. Entendendo por conceitualiza&#231;&#227;o o significado dos conceitos e as rela&#231;&#245;es entre eles, e especifica&#231;&#227;o como a representa&#231;&#227;o formal destes conceitos e rela&#231;&#245;es.
    </p>
    <p>
      Para Guarino (2009), a modelagem de ontologias tem o objetivo de compartilhar e reutilizar o conhecimento em diferentes aplica&#231;&#245;es.
    </p>
    <p>
      
    </p>
    <p>
      <b>Tipos de Ontologias</b>
    </p>
    <p>
      As ontologias s&#227;o constitu&#237;das por um conjunto de conceitos essenciais sobre um dom&#237;nio, representados pelos vocabul&#225;rios especializados; um corpo de conhecimento numa hierarquia de classes (C), um conjunto de rela&#231;&#245;es (R) e restri&#231;&#245;es sem&#226;nticas ou axiomas (A) (ISOTANI, 2015).
    </p>
    <p>
      Que somados ao conjunto de inst&#226;ncias (I), completam o conjunto de elementos de uma ontologia O = {C, R, I, A}. Pode-se classificar as ontologias quanto ao porte em leves ou pesadas, e quanto a &#225;rea de atua&#231;&#227;o de dom&#237;nio ou de tarefa.
    </p>
    <p>
      As leves enfatizam as taxonomias para categorizar grandes volumes de dados, j&#225; as pesadas primam pelo rigor na defini&#231;&#227;o, organiza&#231;&#227;o e formaliza&#231;&#227;o dos conceitos e rela&#231;&#245;es - fundamental para o compartilhamento e reuso de bases de conhecimento.
    </p>
    <p>
      As ontologias de dom&#237;nio descrevem onde as tarefas s&#227;o executadas, representando o conhecimento sobre um assunto. As de tarefa detalham os processos e atividades necess&#225;rias para resolver um problema, isto &#233;, a habilidade de aplicar o conhecimento de um dom&#237;nio; seus conceitos podem ser categorizados em: tasks roles, tasks actions e estados dos objetos/fluxos de informa&#231;&#227;o (MIZOGUCHI, 2004).
    </p>
    <p>
      Existem duas maneiras de representar uma ontologia: formal e gr&#225;fica.
    </p>
    <p>
      A formal &#233; consumida por computadores e utiliza linguagens para a representa&#231;&#227;o, tais como: l&#243;gica de predicados, l&#243;gica descritiva (frames) e descri&#231;&#227;o de ontologias. Desta &#250;ltima, as mais conhecidas (recomenda&#231;&#245;es do W3C) s&#227;o RDF/RDF-S e OWL.
    </p>
    <p>
      A representa&#231;&#227;o gr&#225;fica, destinada a pessoas, utiliza formas como: grafos, UML e estrutura de &#225;rvore. Tanto a representa&#231;&#227;o formal quanto a gr&#225;fica contribuem de forma decisiva para a qualidade da ontologia (LAUFER, 2015).
    </p>
