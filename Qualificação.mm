    <p>
      O objetivo dos cuidados de sa&#250;de &#233; a manuten&#231;&#227;o e a melhoria da sa&#250;de f&#237;sica e mental, especialmente atrav&#233;s da presta&#231;&#227;o de servi&#231;os m&#233;dicos. No DECS, cuidados de sa&#250;de refere-se a todos os aspectos da provis&#227;o e distribui&#231;&#227;o de servi&#231;os de sa&#250;de a uma popula&#231;&#227;o.
    </p>
    <p>
      Os cuidados de sa&#250;de acontecem com as pessoas ao longo do tempo e est&#227;o relacionados a v&#225;rios aspectos: doen&#231;as, prescri&#231;&#245;es, drogas, procedimentos, dispositivos, faturamento (pedidos), genoma, entre outros.
    </p>
    <p>
      Os dados relativos a estes cuidados ficam espalhados nos diversos bancos de dados das in&#250;meras institui&#231;&#245;es de sa&#250;de por onde o indiv&#237;duo &#233; atendido.
    </p>
    <p>
      Significa dizer que a busca por informa&#231;&#245;es pode ser atendida por uma variedade de recursos dispon&#237;veis, que armazenam dados sobre o mesmo dom&#237;nio, mas possuem caracter&#237;sticas diferentes. Portanto, a necessidade de integra&#231;&#227;o e an&#225;lise de dados dessas fontes torna-se mais evidente (Ziegler and Dittrich, 2007).
    </p>
    <p>
      Diante da diversidade, descentralidade, e do volume dos dados de sa&#250;de, para analis&#225;-los e integr&#225;-los eficientemente, &#233; necess&#225;rio o uso de tecnologias que permitam a colabora&#231;&#227;o entre computadores e humanos na Web. As ferramentas da Web Sem&#226;ntica permitem explicitar declara&#231;&#245;es de conhecimento embutidas nos dados. Portanto, tornar esses dados pass&#237;veis de serem lidos/compreendidos por m&#225;quinas (machine-readable) &#233; um passo importante para integra-los.
    </p>
    <p>
      Tim Berners-Lee et al (2000) prop&#244;s o conceito de Web Sem&#226;ntica no seu livro Weaving the Web - The Original Design and Ultimate Destiny of the World Wide Web by Its Inventor. Incentivando a colocar os dados na web de uma forma que as m&#225;quinas pudessem entender.
    </p>
    <p>
      A representa&#231;&#227;o expl&#237;cita da sem&#226;ntica dos dados, aliada a teoria dos dom&#237;nios (ontologias), possibilita um novo quantitativo de servi&#231;os oferecidos pela Web, entre eles, agentes inteligentes, corretores e filtros de informa&#231;&#245;es. (Decker, 2000)
    </p>
    <p>
      Para tornar a Web de Dados uma realidade, &#233; preciso que uma grande quantidade de dados esteja descrita na web - por meio de metadados - num padr&#227;o leg&#237;vel por m&#225;quinas. Mais do que isso, as rela&#231;&#245;es entre esses dados tamb&#233;m precisam estar dispon&#237;veis. Esses conjuntos de dados relacionados na Web tamb&#233;m s&#227;o chamados de Linked Data (Dados Conectados).
    </p>
    <p>
      O Linked Data comp&#245;e a base da Web Sem&#226;ntica e almeja criar um banco de dados global que permita &#224;s aplica&#231;&#245;es raciocinar sobre esses dados. O crescimento deste banco de dados costuma ser demonstrado numa nuvem, chamada LOD - Linked Open Data, cujo n&#243; central &#233; a DBpedia que cont&#233;m dados extra&#237;dos das caixas de informa&#231;&#245;es da Wikipedia.
    </p>
    <p>
      Existe uma divis&#227;o espec&#237;fica na LOD para conjuntos de dados relativos &#224;s Ci&#234;ncias da Vida, composta por ontologias que representam, por exemplo: CID-10, CIAP2, SNOMED, RadLex, NCI Thesaurus, RxNORM, MESH, entre outros.
    </p>
