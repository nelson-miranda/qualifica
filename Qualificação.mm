    </p>
    <p>
      A vers&#227;o 2 da OWL possui diversos perfis, projetados para atender as mais variadas necessidades com diferentes n&#237;veis de expressividade:
    </p>
    <ul>
      <li>
        OWL 2 DL: usado para especificar classes complexas e axiomas;
      </li>
      <li>
        OWL 2 QL: voltado para bancos de dados relacionais (SQL);
      </li>
      <li>
        OWL 2 RL: pr&#243;prio para aplica&#231;&#245;es com necessidades de racioc&#237;nio .
