    </p>
    <p>
      Por outro lado, dados de sa&#250;de podem ser complexos e heterog&#234;neos, como por exemplo, imagens radiol&#243;gicas, anota&#231;&#245;es de profissionais de sa&#250;de, genoma, entre outros.
