      O openEHR &#233; uma arquitetura para criar Registros Eletr&#244;nicos em Sa&#250;de (RES) interoper&#225;veis e independentes da tecnologia utilizada para sua captura. Sua principal caracter&#237;stica &#233; separar o dom&#237;nio cl&#237;nico (onde est&#227;o os profissionais de sa&#250;de) do dom&#237;nio t&#233;cnico (desenvolvedores de software). O openEHR tem como base modelos de conhecimento cl&#237;nicos livres - chamados de arqu&#233;tipos &#8211; que permitem a cria&#231;&#227;o desde sistemas de informa&#231;&#227;o em sa&#250;de simples, tais como aplicativos para tablets, at&#233; complexos sistemas de gest&#227;o hospitalar (openEHR, 2014).
    </p>
    <p>
      No contexto cl&#237;nico tais conhecimentos s&#227;o organizados e representados na forma de arqu&#233;tipos e templates (agrupamentos de arqu&#233;tipos). Para modelar e representar estes conhecimentos, os profissionais de sa&#250;de fazem uso de uma plataforma com ferramentas de modelagem e formalizam em artefatos process&#225;veis por m&#225;quinas e depositados em reposit&#243;rios chamados de Clinical Knowledge Management - CKM.
    </p>
    <p>
      O openEHR pode ser associado a diversas terminologias, tais como a Classifica&#231;&#227;o Internacional de Doen&#231;as (CID-10), a ontologia SNOMED-CT, al&#233;m de terminologias internas na forma de tabelas de dom&#237;nio.
